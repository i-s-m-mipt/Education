#include <algorithm>
#include <cassert>
#include <iterator>
#include <regex>
#include <string>
#include <vector>

/////////////////////////////////////////////////////////////////////////////////////

using namespace std::literals;

/////////////////////////////////////////////////////////////////////////////////////

int main()
{
    auto string = R"(aaaaa!"#$%&'()*+,-./:;<=>?@[\]^_`{|}~AAAAA)"s;

//  ---------------------------------------------------------------------------------

    std::regex pattern(R"(a{5})", std::regex_constants::icase);

//  ---------------------------------------------------------------------------------

    std::vector < std::string > strings;

//  ---------------------------------------------------------------------------------

    auto lambda = [&strings](auto const & matches){ strings.push_back(matches[0]); };

//  ---------------------------------------------------------------------------------

    std::ranges::for_each
    (
        std::sregex_iterator(std::cbegin(string), std::cend(string), pattern),

        std::sregex_iterator(),

        lambda
    );

//  ---------------------------------------------------------------------------------

    assert(strings == std::vector < std::string > ({ "aaaaa", "AAAAA" }));
}

