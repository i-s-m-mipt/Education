///////////////////////////////////////////////////////////////////////////

#include <cstddef>
#include <mdspan>

///////////////////////////////////////////////////////////////////////////

int main()
{
    auto const size = 5uz;

//  -----------------------------------------------------------------------

	auto array = new int[size * size]{};

//  -----------------------------------------------------------------------

	std::mdspan mdspan(array, std::extents < std::size_t, size, size > ());

//  -----------------------------------------------------------------------

	for (auto i = 0uz; i < mdspan.extent(0); ++i)
	{
		for (auto j = 0uz; j < mdspan.extent(1); ++j)
		{
			mdspan[i, j] = j + 1;
		}
	}

//  -----------------------------------------------------------------------

	delete[] array;
}

