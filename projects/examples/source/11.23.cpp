#include <algorithm>
#include <cassert>
#include <functional>
#include <vector>

////////////////////////////////////////////////////////////////////////////

struct Entity 
{ 
	int x = 0;
};

////////////////////////////////////////////////////////////////////////////

int main()
{
    std::vector < Entity > entities = { { 5 }, { 4 }, { 3 }, { 2 }, { 1 } };

//  ------------------------------------------------------------------------

	std::ranges::sort(entities, std::less(), &Entity::x);

//  ------------------------------------------------------------------------

	assert(std::ranges::is_sorted(entities, std::less(), &Entity::x));
}

