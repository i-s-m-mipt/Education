#include <algorithm>
#include <cassert>
#include <iterator>
#include <regex>
#include <string>
#include <vector>

////////////////////////////////////////////////////////////////////////////////////////////

using namespace std::literals;

////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    auto string = R"(aaaaa!"#$%&'()*+,-./:;<=>?@[\]^_`{|}~AAAAA)"s;

//  ----------------------------------------------------------------------------------------

    std::regex pattern(R"([[:punct:]]+)", std::regex_constants::icase);

//  ----------------------------------------------------------------------------------------

    std::vector < std::string > strings;

//  ----------------------------------------------------------------------------------------

    auto lambda = [&strings](auto const & match){ strings.push_back(match); };

//  ----------------------------------------------------------------------------------------

    std::ranges::for_each
    (
        std::sregex_token_iterator(std::cbegin(string), std::cend(string), pattern, { -1 }),

        std::sregex_token_iterator(),

        lambda
    );

//  ----------------------------------------------------------------------------------------

    assert(strings == std::vector < std::string > ({ "aaaaa", "AAAAA" }));
}

