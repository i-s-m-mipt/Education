///////////////////////////////////////////////////////////////////////////////

#include <algorithm>
#include <cassert>
#include <set>

///////////////////////////////////////////////////////////////////////////////

struct Entity 
{ 
	int x = 0;
};

///////////////////////////////////////////////////////////////////////////////

class Less
{
public :

	auto operator()(Entity const & lhs, Entity const & rhs) const
	{
		return lhs.x < rhs.x;
	}
};

///////////////////////////////////////////////////////////////////////////////

int main()
{
	std::set < Entity, Less > entities = { { 5 }, { 4 }, { 3 }, { 2 }, { 1 } };

//  ---------------------------------------------------------------------------

	assert(std::ranges::is_sorted(entities, Less()));
}

