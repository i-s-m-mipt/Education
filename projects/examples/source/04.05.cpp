//////////////////////////////////////////////////////////////////////////

#include <print>

//////////////////////////////////////////////////////////////////////////

void show_v1()
{
	std::print("show_v1\n");
}

//////////////////////////////////////////////////////////////////////////

template < typename T, typename ... Ts > void show_v1(T x, Ts ... xs)
{
	std::print("show_v1 : x = {} sizeof...(xs) = {}\n", x, sizeof...(xs));

	show_v1(xs...); // support : cppinsights.io
}

//////////////////////////////////////////////////////////////////////////

void show_v2(auto x)
{
	std::print("show_v2 : x = {} ", x);
}

//////////////////////////////////////////////////////////////////////////

void show_v2(auto x, auto ... xs)
{
	show_v2(x);

	std::print("sizeof...(xs) = {}\n", sizeof...(xs));

	show_v2(xs...);

	if (sizeof...(xs) == 1)
	{
		std::print("sizeof...(xs) = 0\n");
	}
}

//////////////////////////////////////////////////////////////////////////

void transform(auto ... xs)
{
	show_v1(xs * xs...); // support : cppinsights.io
}

//////////////////////////////////////////////////////////////////////////

struct Entity 
{
	int x = 0, y = 0;
};

//////////////////////////////////////////////////////////////////////////

auto make_entity(auto ... xs)
{
	return new Entity(xs...);
}

//////////////////////////////////////////////////////////////////////////

int main()
{
	show_v1(1, 2.0, "aaaaa");

	show_v2(1, 2.0, "aaaaa");

//  -------------------------

	transform(1, 2, 3);

//  -------------------------

    delete make_entity(1, 1);
}

