#include <bitset>
#include <cassert>

/////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    assert((std::bitset < 8 > (0b0011) & std::bitset < 8 > (0b0101)) == 0b0001);

    assert((std::bitset < 8 > (0b0011) | std::bitset < 8 > (0b0101)) == 0b0111);

//  -------------------------------------------------------------------------------------

    assert((std::bitset < 8 > (std::bitset < 8 > (255).to_string()).to_ullong()) == 255);
}

