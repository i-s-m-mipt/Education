#include <cassert>

//////////////////////////////////////////////////////////////////

inline auto test_v1(int x, int y)
{ 
	return x + y;
}

//////////////////////////////////////////////////////////////////

__attribute__((noinline)) auto test_v2(int x, int y)
{
	return x + y;
}

//////////////////////////////////////////////////////////////////

int main()
{
	auto volatile x = 1, y = 2;

//  --------------------------------------------------------------

	assert(test_v1(x, y) == 3); // support : compiler-explorer.com

	assert(test_v2(x, y) == 3); // support : compiler-explorer.com

	assert(test_v1(1, 2) == 3); // support : compiler-explorer.com

	assert(test_v2(1, 2) == 3); // support : compiler-explorer.com
}

