////////////////////////////////////////////////////////////////////////////////////////

#include <zipper/3.0.2/Zipper/Zipper.hpp>

////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    using Zipper = zipper::Zipper;

//  ------------------------------------------------------------------------------------

    Zipper zipper("output.zip", Zipper::OpenFlags::Overwrite);

//  ------------------------------------------------------------------------------------

    zipper.add("image.jpg", Zipper::ZipFlags::Better | Zipper::ZipFlags::SaveHierarchy);

//  ------------------------------------------------------------------------------------

    zipper.close();
}

