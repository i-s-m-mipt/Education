#include <cassert>
#include <regex>
#include <string>

/////////////////////////////////////////////////////////////////////

auto search(std::string const & string)
{
    std::smatch matches;

    std::regex_search(string, matches, std::regex(R"(a{5}(b{5})?)"));

    return matches;
}

/////////////////////////////////////////////////////////////////////

int main()
{
    assert(search(" aaaaa      ")[0] == "aaaaa"     );

    assert(search(" aaaaabbbbb ")[0] == "aaaaabbbbb");

    assert(search(" aaaaabbbbb ")[1] ==      "bbbbb");
}

