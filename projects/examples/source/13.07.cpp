///////////////////////////////////////////////////////////////////////////////////////////////

#include <cassert>
#include <ostream>
#include <sstream>

///////////////////////////////////////////////////////////////////////////////////////////////

template < typename T > struct Adapter
{
    T const & x;
};

///////////////////////////////////////////////////////////////////////////////////////////////

template < typename T > auto & operator<<(std::ostream & stream, Adapter < T > const & adapter)
{
    return stream << adapter.x << ' ';
}

///////////////////////////////////////////////////////////////////////////////////////////////

auto test(auto ... xs)
{
//  return (std::stringstream() << ... <<         xs ).str(); // bad

    return (std::stringstream() << ... << Adapter(xs)).str();
}

///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    assert(test(1, 2.0, "aaaaa") == "1 2 aaaaa ");
}

