#include <algorithm>
#include <array>
#include <cassert>

/////////////////////////////////////////////////////////////////////////////////////////////////

#include <boost/asio.hpp>
#include <boost/cobalt.hpp>

/////////////////////////////////////////////////////////////////////////////////////////////////

boost::cobalt::main co_main(int, char * [])
{
    boost::asio::ip::tcp::socket socket(co_await boost::cobalt::this_coro::executor);

//  ---------------------------------------------------------------------------------------------

    auto address = boost::asio::ip::make_address("127.0.0.1");

//  ---------------------------------------------------------------------------------------------

    boost::asio::ip::tcp::endpoint endpoint(address, 80);

//  ---------------------------------------------------------------------------------------------

    co_await socket.async_connect(endpoint, boost::cobalt::use_op);

//  ---------------------------------------------------------------------------------------------

    std::array < char, 1 << 10 > array = {};

//  ---------------------------------------------------------------------------------------------

    std::ranges::fill(array, 'a');

//  ---------------------------------------------------------------------------------------------

    co_await boost::asio::async_write(socket, boost::asio::buffer(array), boost::cobalt::use_op);

    co_await boost::asio::async_read (socket, boost::asio::buffer(array), boost::cobalt::use_op);

//  ---------------------------------------------------------------------------------------------

    auto lambda = [](auto x){ assert(x == 'a'); };

//  ---------------------------------------------------------------------------------------------

    std::ranges::for_each(array, lambda);

//  ---------------------------------------------------------------------------------------------

    co_return 0;
}

