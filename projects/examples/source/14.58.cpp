// content : Managed Shared Memory

/////////////////////////////////////////////////////////////////////////////

#include <cassert>

/////////////////////////////////////////////////////////////////////////////

#include <boost/interprocess/managed_shared_memory.hpp>

/////////////////////////////////////////////////////////////////////////////

int main()
{
    using shared_memory_t = boost::interprocess::managed_shared_memory;

//  -------------------------------------------------------------------------

	auto path = "output";

//  -------------------------------------------------------------------------

	boost::interprocess::shared_memory_object::remove(path);

//  -------------------------------------------------------------------------

	shared_memory_t storage(boost::interprocess::create_only, path, 1 << 10);

//  -------------------------------------------------------------------------

	storage.construct < int > ("x")(1);

//  -------------------------------------------------------------------------

    auto [x, unused] = storage.find < int > ("x");

//  -------------------------------------------------------------------------

    assert(*x == 1 && unused == 1);

//  -------------------------------------------------------------------------

    storage.construct < int > ("array")[5](0);

//  -------------------------------------------------------------------------

    auto [array, size] = storage.find < int > ("array");

//  -------------------------------------------------------------------------

    array[0] = 1;

//  -------------------------------------------------------------------------

    assert(size == 5 && array[0] == 1);

//  -------------------------------------------------------------------------

	boost::interprocess::shared_memory_object::remove(path);
}

