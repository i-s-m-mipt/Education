#include <cassert>
#include <iterator>
#include <string_view>
#include <utility>

/////////////////////////////////////////////////////////////////////////////////////

using namespace std::literals;

/////////////////////////////////////////////////////////////////////////////////////

#include <boost/fusion/include/std_pair.hpp>
#include <boost/spirit/home/x3.hpp>

/////////////////////////////////////////////////////////////////////////////////////

int main()
{
    auto view = "{ 1, 1 }"sv;

//  ---------------------------------------------------------------------------------

    auto rule =
    (
        '{'
            >> boost::spirit::x3::int_ >> ','

            >> boost::spirit::x3::int_ >>
        '}'
    );

//  ---------------------------------------------------------------------------------

    std::pair < int, int > pair;

//  ---------------------------------------------------------------------------------

    boost::spirit::x3::phrase_parse
    (
        std::begin(view), std::end(view), rule, boost::spirit::x3::ascii::space, pair
    );

//  ---------------------------------------------------------------------------------

    assert(pair == std::make_pair(1, 1));
}

