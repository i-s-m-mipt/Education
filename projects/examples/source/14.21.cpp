#include <iostream>
#include <print>
#include <syncstream>
#include <thread>
#include <vector>

///////////////////////////////////////////////////////////////////////////////////////

void test_v1(int x)
{
    for (auto i = 0uz; i < 5; ++i)
    {
        std::cout << "test_v1" << " : " << "x" << " = " << x << '\n';
    }
}

///////////////////////////////////////////////////////////////////////////////////////

void test_v2(int x)
{
    for (auto i = 0uz; i < 5; ++i)
    {
        std::osyncstream(std::cout) << "test_v2" << " : " << "x" << " = " << x << '\n';
    }
}

///////////////////////////////////////////////////////////////////////////////////////

int main()
{
    {
        std::vector < std::jthread > threads;

    //  --------------------------------------------------------------------

        for (auto i = 1uz; i < std::thread::hardware_concurrency() + 1; ++i)
        {
            threads.emplace_back(test_v1, i);
        }
    }

//  ------------------------------------------------------------------------

    std::print("\n");

//  ------------------------------------------------------------------------

    {
        std::vector < std::jthread > threads;

    //  --------------------------------------------------------------------

        for (auto i = 1uz; i < std::thread::hardware_concurrency() + 1; ++i)
        {
            threads.emplace_back(test_v2, i);
        }
    }
}

