#include <cassert>
#include <regex>
#include <string>

///////////////////////////////////////////////////////////

auto match(std::string const & string)
{
	return std::regex_match(string, std::regex(R"(a{5})"));
}

///////////////////////////////////////////////////////////

int main()
{
    assert(match("aaaaa") == 1);

	assert(match("bbbbb") == 0);
}

