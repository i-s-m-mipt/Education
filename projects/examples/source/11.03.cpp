#include <algorithm>
#include <cassert>
#include <functional>
#include <set>
#include <vector>

////////////////////////////////////////////////////////////////

int main()
{
    std::vector < int > vector = { 5, 4, 3, 2, 1 };

//  ------------------------------------------------------------

	std::less < int > less;

//  ------------------------------------------------------------

	std::ranges::sort(vector, less);

//  ------------------------------------------------------------

	assert(std::ranges::is_sorted(vector, less));

//  ------------------------------------------------------------

	std::set < int, std::less < int > > set = { 5, 4, 3, 2, 1 };

//  ------------------------------------------------------------

	assert(std::ranges::is_sorted(set, less));
}

