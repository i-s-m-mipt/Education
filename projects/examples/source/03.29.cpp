#include <print>

////////////////////////////////////////////////////////////////

class Entity
{
public :

	void test() const &  { std::print("Entity::test (1)\n"); }

	void test() const && { std::print("Entity::test (2)\n"); }
};

////////////////////////////////////////////////////////////////

auto   make_entity_v1() {        Entity entity; return entity; }
	  
auto & make_entity_v2() { static Entity entity; return entity; }

////////////////////////////////////////////////////////////////

int main()
{		
	make_entity_v1().test();
		
	make_entity_v2().test();
}

