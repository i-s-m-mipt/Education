#include <cassert>
#include <regex>
#include <string>

//////////////////////////////////////////////////////////////////////

auto replace(std::string const & string)
{
    return std::regex_replace(string, std::regex(R"(a{5})"), "bbbbb");
}

//////////////////////////////////////////////////////////////////////

int main()
{
    assert(replace("aaaaa") == "bbbbb");
}

