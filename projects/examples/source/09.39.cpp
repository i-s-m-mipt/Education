#include <memory_resource>

///////////////////////////////////////////////////////////

int main()
{
    auto resource = std::pmr::new_delete_resource();

//  -------------------------------------------------------

    auto x = static_cast < int * > (resource->allocate(4));

//  -------------------------------------------------------

    *x = 1;

//  -------------------------------------------------------

    resource->deallocate(x, 4);
}

