#include <cassert>
#include <iterator>
#include <string_view>

////////////////////////////////////////////////////////

using namespace std::literals;

////////////////////////////////////////////////////////

#include <boost/spirit/home/x3.hpp>

////////////////////////////////////////////////////////

int main()
{
    auto view = "1"sv;

//  ----------------------------------------------------
        
    auto begin = std::begin(view), end = std::end(view);

//  ----------------------------------------------------

    auto rule = boost::spirit::x3::int_;

//  ----------------------------------------------------

    auto x = 0;

//  ----------------------------------------------------

    boost::spirit::x3::parse(begin, end, rule, x);

//  ----------------------------------------------------

    assert(x == 1);
}

