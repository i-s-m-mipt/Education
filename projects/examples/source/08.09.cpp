//////////////////////////////////////////////////////////////////////////////////////////

#include <bit>
#include <cassert>
#include <cstddef>

//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    assert(std::endian::native == std::endian::little);

//  --------------------------------------------------------------------------------------

    auto x = 0x01020304;

//  --------------------------------------------------------------------------------------

    auto array = reinterpret_cast < std::byte * > (&x);

//  --------------------------------------------------------------------------------------

    for (auto i = 0uz; i < sizeof(x); ++i)
    {
        assert(std::to_integer < int > (array[i]) == static_cast < int > (sizeof(x) - i));
    }

//  --------------------------------------------------------------------------------------

    auto y = std::byteswap(x);

//  --------------------------------------------------------------------------------------

    array = reinterpret_cast < std::byte * > (&y);

//  --------------------------------------------------------------------------------------

    for (auto i = 0uz; i < sizeof(x); ++i)
    {
        assert(std::to_integer < int > (array[i]) == static_cast < int > (i + 1));
    }
}

