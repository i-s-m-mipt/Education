#include <array>
#include <cassert>
#include <cstring>
#include <sstream>
#include <string>
#include <type_traits>

//////////////////////////////////////////////////////////////////////

using namespace std::literals;

//////////////////////////////////////////////////////////////////////

int main()
{
    [[maybe_unused]] char array_1[5]{ 'a', 'a', 'a', 'a', 'a' };

    [[maybe_unused]] char array_2[6]{ 'a', 'a', 'a', 'a', 'a', '\0' };

//  ------------------------------------------------------------------

    std::array < char, 1 << 10 > array_3 = {};

//  ------------------------------------------------------------------

    auto string = "aaaaa";

//  ------------------------------------------------------------------

    static_assert(std::is_same_v < decltype(string), char const * > );

//  ------------------------------------------------------------------

    assert(std::strlen("aaaaa"s.c_str()) == 5);

//  ------------------------------------------------------------------

    std::stringstream stream_1("aaaaa");

    std::stringstream stream_2;

//  ------------------------------------------------------------------

    auto begin = std::begin(array_3);
        
//  ------------------------------------------------------------------
    
    stream_1.getline(begin, std::size(array_3));

    stream_2 << begin;

//  ------------------------------------------------------------------

    assert(stream_2.str() == stream_1.str());
}

