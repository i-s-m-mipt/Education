#include <array>
#include <cassert>
#include <cstring>
#include <iterator>
#include <sstream>
#include <string>
#include <type_traits>

//////////////////////////////////////////////////////////////////////

using namespace std::literals;

//////////////////////////////////////////////////////////////////////

int main()
{
    auto string = "aaaaa";

//  ------------------------------------------------------------------

    static_assert(std::is_same_v < decltype(string), char const * > );

//  ------------------------------------------------------------------

    assert(std::strlen("aaaaa"s.c_str()) == 5);

//  ------------------------------------------------------------------

    std::stringstream stream_1("aaaaa");

    std::stringstream stream_2;

//  ------------------------------------------------------------------

    std::array < char, 1 << 10 > array = {};

//  ------------------------------------------------------------------

    stream_1.getline(std::begin(array), std::size(array), '\n');

//  ------------------------------------------------------------------

    stream_2 << std::data(array);

//  ------------------------------------------------------------------

    assert(stream_2.str() == stream_1.str());
}

