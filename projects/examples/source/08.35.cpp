#include <print>

//////////////////////////////////////////////////////////////////////////////

#include <boost/asio.hpp>

//////////////////////////////////////////////////////////////////////////////

void test(boost::system::error_code const &)
{
    std::print("test\n");
}

//////////////////////////////////////////////////////////////////////////////

int main()
{
    boost::asio::io_service service;

//  --------------------------------------------------------------------------

    boost::asio::deadline_timer timer(service, boost::posix_time::seconds(5));

//  --------------------------------------------------------------------------

    timer.async_wait(&test);

//  --------------------------------------------------------------------------

    service.run();
}

