#include <cassert>
#include <cstddef>

/////////////////////////////////////////////////////////////////////////

int main()
{
    assert((std::byte(0b0011) & std::byte(0b0101)) == std::byte(0b0001));

    assert((std::byte(0b0011) | std::byte(0b0101)) == std::byte(0b0111));
}

