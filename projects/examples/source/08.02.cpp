#include <cassert>

/////////////////////////////////////////

int main()
{
    assert((0b0011 &  0b0101) == 0b0001);

    assert((0b0011 & ~0b0101) == 0b0010);

    assert((0b0011 |  0b0101) == 0b0111);

    assert((0b0011 ^  0b0101) == 0b0110);

    assert((0b0011 >> 0b0010) == 0b0000);

    assert((0b0011 << 0b0010) == 0b1100);

//  -------------------------------------

    auto x = 1, y = 2;

//  -------------------------------------

    x ^= y;

    y ^= x;

    x ^= y;

//  -------------------------------------

    assert(x == 2 && y == 1);

//  -------------------------------------

    if (x > 1)
    {
        ++y;
    }

//  -------------------------------------

    y += ((1 - x) >> 31) & 1;

//  -------------------------------------

    assert(y == 3);
}

