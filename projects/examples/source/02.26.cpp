#include <cassert>
#include <print>
#include <type_traits>

/////////////////////////////////////////////////////////////////////////

int main()
{
    auto x = 1, * y = &x;

//  ---------------------------------------------------------------------

	static_assert(std::is_same_v < decltype(y), int * > );

//  ---------------------------------------------------------------------

	std::print("main : y = {}\n", static_cast < void * > (y));

//  ---------------------------------------------------------------------

	assert(*y == x);

//  ---------------------------------------------------------------------

	[[maybe_unused]] int * z1 = nullptr;

//  ---------------------------------------------------------------------

	static_assert(std::is_same_v < decltype(nullptr), std::nullptr_t > );

//  ---------------------------------------------------------------------

//	[[maybe_unused]] int * z2 = 0; // bad
}

