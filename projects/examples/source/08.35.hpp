#include <chrono>

///////////////////////////////////////////////////////////////////////////

template < typename D = std::chrono::duration < double > > class Timer
{
public :

	Timer() : m_begin(clock_t::now()) {}

//  -----------------------------------------------------------------------

	auto elapsed() const
	{
		return std::chrono::duration_cast < D > (clock_t::now() - m_begin);
	}

private :

    using clock_t = std::chrono::steady_clock;

//  -----------------------------------------------------------------------
	
	clock_t::time_point m_begin;
};

