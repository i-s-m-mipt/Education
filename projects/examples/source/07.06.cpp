#include <cassert>

//////////////////////////////////////////////////////

struct Alternative 
{
	union
	{
		int x, y = 0;
	};

//  -----------------------
	
	bool has_error = false;
};

//////////////////////////////////////////////////////

auto make_alternative(int x)
{
	if (x > 0)
	{
		return Alternative { .x = x, .has_error = 0 };
	}
	else
	{
		return Alternative { .y = x, .has_error = 1 };
	}
}

//////////////////////////////////////////////////////

int main()
{
	assert(make_alternative(0).has_error == 1);

	assert(make_alternative(1).has_error == 0);
}

