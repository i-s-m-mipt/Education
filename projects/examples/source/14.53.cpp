#include <cassert>

////////////////////////////////////////////////////////////////////////////////

#include <boost/process.hpp>

////////////////////////////////////////////////////////////////////////////////

int main()
{
	boost::process::child process_1("14.54", boost::process::args({ "aaaaa" }));

    boost::process::child process_2("14.54", boost::process::args({ "bbbbb" }));

//  ----------------------------------------------------------------------------

	process_1.join();

//  ----------------------------------------------------------------------------

    assert(process_1.exit_code() == 0);

//  ----------------------------------------------------------------------------

    process_2.terminate();
}

